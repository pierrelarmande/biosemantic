package com.wscreation.sesame;

public class SesameParameters {
	
	//ATTRIBUTS
	private String repoID="";
	private String serveurURL="";
	private String repoTitle="";
	private String mappingPath="";
	private String password="";
	private String login="";

	//CONSTRUCTORS
	public SesameParameters(String repoID, String serveurURL, String repoTitle, String mappingPath, String login, String password){
		this.repoID=repoID;
		this.repoTitle=repoTitle;
		this.serveurURL=serveurURL;
		this.mappingPath=mappingPath;
		this.login=login;
		this.password=password;
	}
	
	//ACCESSORS
	public String getRepoID(){return repoID;}
	public String getServeurURL(){return serveurURL;}
	public String getRepoTitle(){return repoTitle;}
	public String getMappingPath(){return mappingPath;}
	public String getLogin(){return login;}
	public String getPassword(){return password;}
	
	


}
